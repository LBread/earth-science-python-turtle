import turtle
t = turtle.Turtle()
t.speed(0)
t.penup()
t.goto(0,-200)
def layer(radius, color_, y_level):
    t.sety(y_level)
    t.begin_fill()
    t.color(color_)
    t.circle(radius)
    t.end_fill()
layer(150,"OliveDrab",-190)
layer(140,"#873A1B",-180)
layer(120,"#9E3018",-160)
layer(70,"#CC7A1D",-110)
layer(35,"#D99C18",-75)
def create_key_of_things(colortext,x,y,text):
    t.penup()
    t.color(colortext) 
    t.goto(x,y) 
    t.write(text, font=("Arial", 10, "bold"), align="left")
    t.goto(x-5.5,y+1.5)
    t.begin_fill()
    t.circle(2.5,360,4)
    t.end_fill()
create_key_of_things("OliveDrab",-170,185, "Lithosphere = outer layer made of rock and dirt")
create_key_of_things("#873A1B",-170,170,"Ashthenosphere = inner layer made of minerals") 
create_key_of_things("#9E3018",-170,155,"Mesosphere = center layer made of ores and lava") 
create_key_of_things("#CC7A1D",-170,140,"Outercore = lower layer made of molten metal and lava") 
create_key_of_things("#D99C18",-170,125,"Innercore = lowest layer made of hot iron")
